using AbstractFactory.StarCraft.Energia;
using AbstractFactory.StarCraft.Interfaces.Fabrica;
using AbstractFactory.StarCraft.Revestimento;

namespace AbstractFactory.StarCraft.Fabrica
{
    internal class FabricaBaseZerg : IFabricaBases
    {
        public FabricaBaseZerg()
        {
            CriarBase();
        }

        public void CriarBase()
        {
            Console.WriteLine("Base Zerg criada com sucesso!");

            RevestimentoBaseZerg revestimento = new RevestimentoBaseZerg();
            revestimento.Composicao();

            EnergiaBaseZerg energia = new EnergiaBaseZerg();
            energia.Composicao();
        }
    }
}
